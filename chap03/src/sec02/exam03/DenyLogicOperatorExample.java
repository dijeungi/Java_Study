package sec02.exam03;

public class DenyLogicOperatorExample {
	public static void main(String[] args) {
		
		boolean play = true;
		System.out.println(play);
		
		play = !play; // false 로 변경
		System.out.println(play);
		
		play = !play; // true 로 변경
		System.out.println(play);

	}

}
